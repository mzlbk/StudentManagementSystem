public class Areas
{
    private String areasName;

    public String getAreasName()
    {
        return areasName;
    }

    public void setAreasName(String areasName)
    {
        this.areasName = areasName;
    }

    public Areas()
    {
        String areasName = "NULL";
    }

    public Areas(String areasName)
    {
        areasName = this.areasName;
    }
}
