public class Areas
{
    private int areasCode;

    public int getAreasCode() {
        return areasCode;
    }

    public void setAreasCode(int areasCode) {
        this.areasCode = areasCode;
    }

    public Areas()
    {
        int areasCode = 0;
    }

    public Areas(int areasCode)
    {
        areasCode = this.areasCode;
    }
}
